sub GetSources 
{
	#print "$File::Find::name\n";
	if ($File::Find::name =~ /\.bas/)
	{
		my $File = $File::Find::name;
		$File =~ s/\\/\//g;
		my $SearchDir = $File::Find::topdir;
		$SearchDir =~ s/\\/\//g;
		$File = "ExportedFiles" . $1 if ($File =~ /$SearchDir(.*)/);
		#print "$File::Find::name :: $File::Find::topdir\n";
		#print "$File\n";
		push (@VBFiles, $File);
	}

	if ($File::Find::name =~ /\.txt/)
	{
		my $File = $File::Find::name;
		$File =~ s/\\/\//g;
		my $SearchDir = $File::Find::topdir;
